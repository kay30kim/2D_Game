#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, t_size_t dstsize)
{
	unsigned long	i;

	if (!dst || !src)
		return (0);
	i = 0;
	if (dstsize != 0)
	{
		while (src[i] && i < dstsize - 1)
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = 0;
	}
	return (ft_strlen(src));
}
