#include "ft_printf.h"

int	ft_print_s(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[i])
		ft_putchar_fd(str[i++], 1);
	return (ft_strlen(str));
}
